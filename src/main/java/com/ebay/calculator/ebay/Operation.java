package com.ebay.calculator.ebay;

/**
 * Enum representing different mathematical operations.
 * <p>
 * The {@code Operation} enum defines the set of supported operations that can be performed by the
 * {@link Calculator} service. Each enum constant represents a specific mathematical operation.
 * </p>
 */
public enum Operation {
    /**
     * Represents addition operation.
     */
    ADD,

    /**
     * Represents subtraction operation.
     */
    SUBTRACT,

    /**
     * Represents multiplication operation.
     */
    MULTIPLY,

    /**
     * Represents division operation.
     */
    DIVIDE
}
